+ (CGFloat)scaleForPickImage:(UIImage *)image maxWidthPixelSize:(CGFloat)maxWidthPixelSize
{
    if (!image) {
        return 1.f;
    }
    CGFloat scale = 1.f;
    CGSize imageSize = image.size;
    if (imageSize.width > maxWidthPixelSize) {
        scale = maxWidthPixelSize / imageSize.width;
    }
    return scale;
}

