    }
    
    NSString *sex = [UserInfoTool getUserInfo].sex;
    NSString *sexName = @"";
    if ([sex intValue] == 1) {
        sexName = @"男";
    }else if ([sex intValue] == 2) {
        sexName = @"女";
    }else if ([sex intValue] == 4) {
        sexName = @"保密";
