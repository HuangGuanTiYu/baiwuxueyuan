- (void)viewDidLoad {
    [super viewDidLoad];
    // Do any additional setup after loading the view.
    
    self.title = @"个人资料";
    
    self.titles = @[@"头像",@"昵称",@"性别",@"手机号",@"邮箱",@"",@"工号",@"部门",@"公司"];
